#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t			i;
	unsigned int	res;
	int				neg;

	i = 0;
	res = 0;
	neg = 1;
	while (str[i] == 127 || (str[i] <= 32 && str[i] >= 0 && str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i])
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return ((int)(res * neg));
}
