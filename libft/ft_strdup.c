#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*cpy;
	size_t	i;
	size_t	len;

	len = ft_strlen((char*)str);
	i = 0;
	if ((cpy = (char*)malloc(sizeof(char) * len + 1)) == NULL)
		return (NULL);
	while (str[i])
	{
		cpy[i] = str[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
